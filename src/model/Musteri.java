package model;

/**
 *
 * @author Mert
 */
public class Musteri {
    private int musteriId;
    private String ad;
    private String soyad;
    private String tc;
    private String adres;
    private String tel;

    public Musteri(String ad, String soyad, String tc, String adres, String tel) {
        this.ad = ad;
        this.soyad = soyad;
        this.tc = tc;
        this.adres = adres;
        this.tel = tel;
    }

    public int getMusteriId() {
        return musteriId;
    }

    public void setMusteriId(int musteriId) {
        this.musteriId = musteriId;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getSoyad() {
        return soyad;
    }

    public void setSoyad(String soyad) {
        this.soyad = soyad;
    }

    public String getTc() {
        return tc;
    }

    public void setTc(String tc) {
        this.tc = tc;
    }

    public String getAdres() {
        return adres;
    }

    public void setAdres(String adres) {
        this.adres = adres;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }
    
    
    
}
